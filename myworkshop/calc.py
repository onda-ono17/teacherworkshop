# 사칙연산을 계산하는 샘플코드

# 두 수의 합을 반환합니다.
def add(a: float, b: float) -> float:
	return a + b


# 두 수의 차를 반환합니다.
def subtract(a: float, b: float) -> float:
	return a - b


# 두 수의 곱을 반환합니다.
def multiply(a: float, b: float) -> float:
	return a * b


# 두 수를 나누고 0으로 나누는 경우 예외를 발생시킵니다.
def divide(a: float, b: float) -> float:
	if b == 0:
		raise ValueError("0으로 나눌 수 없습니다.")
	return a / b


# 샘플 입력값으로 사칙연산 결과를 출력합니다.
if __name__ == "__main__":
	x = 10
	y = 5

	print(f"덧셈: {x} + {y} = {add(x, y)}")
	print(f"뺄셈: {x} - {y} = {subtract(x, y)}")
	print(f"곱셈: {x} * {y} = {multiply(x, y)}")
	print(f"나눗셈: {x} / {y} = {divide(x, y)}")



